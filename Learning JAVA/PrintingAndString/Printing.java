package PrintingAndString;

public class Printing {
    public static void main(String args[]){
        int x=10,y=5;
        
        System.out.println(x+y);

        System.out.println(x+y+"Sum");

        System.out.println("Sum"+x+y);

        System.out.println("Sum"+(x+y));

        System.out.println("Sum of "+x+" and "+y+" is: "+(x+y));
    }
}
