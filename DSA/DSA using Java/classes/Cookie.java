package classes;

public class Cookie {
    //property
    private String color;
    public int length;

    //constructor
    public Cookie(String color){
        this.color = color;
    }

    public Cookie(int length){
        this.length = length;
    }

    //getter and setters
    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    
    
}
